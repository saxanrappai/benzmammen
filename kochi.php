	Kochi, formerly Cochin, is a major port city on the west coast of India by the Arabian Sea and is part of the district of Ernakulam of the state of Kerala. Kochi is often called by the name Ernakulam, which refers to the eastern part of the mainland Kochi. The civic body that governs the city is the Corporation of Cochin, which was constituted in the year 1967, and the statutory bodies that oversee its development are the Greater Cochin Development Authority (GCDA) and the Goshree Islands Development Authority (GIDA).<br/><br/>
